package Arrays;

public class ForeEach1 {
    public static void main(String[] args) {
        int[]forForeach={1,3,5,7,9,0,2,4};
        for(int number:forForeach){
            System.out.println(number);
        }

        String[]forForeach1={"Alabama", "Utah", "Oregon","Virginia","Texsas"};;
        for(String number:forForeach1){
            System.out.println(number);
        }


    }
}
