package patterns;

public class Square {
    public static void main(String[] args) {
        for (int j = 1; j <= 4; j++) {                                       //nested for loop
            for (int i = 1; i <= 4; i++) {
                System.out.print("* ");

            }
            System.out.println();

        }
        System.out.println();


    }

}
