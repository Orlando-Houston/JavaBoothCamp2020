package Jul;

public class FirstFor {
    public static void main(String[] args) {
        for (int i=0;i<=10;i++){
            System.out.println("Hello World");
            System.out.println("Mesaj");
            System.out.println("Mesaj" +i);
        }
        int i=0;
        while (i<10);{
            System.out.println("mesaj"+i);
            i++;
        }
    }

}
