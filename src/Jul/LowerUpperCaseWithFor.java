package Jul;

public class LowerUpperCaseWithFor {
    public static void main(String[] args) {
        //all uppercase letters
        for (int i = 65; i < 91; i++) {
            System.out.println((char) i);
        }
        for(int j=97;j<=122;j++){
            System.out.println((char)j);
        }


    }
}
