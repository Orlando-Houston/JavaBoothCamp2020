package Jul;

public class ForLoop4 {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("" + (i + 1));
        }
        for (int i = 1; i <= 10; i++) {
            System.out.println("" + i);

        }
        for (int i = 1; i <= 20; i = i + 2) {
            System.out.println("" + i);

        }
        for (int i = 1; i <= 20; i+=2) {
            System.out.println("" + i);
        }
        for (int i=0; i<10;i++){
            System.out.println(""+(2*i+1));
        }
    }

}
